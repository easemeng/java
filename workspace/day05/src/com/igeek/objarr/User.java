package com.igeek.objarr;

public class User {

	@SuppressWarnings("unused")
	private String username;
	@SuppressWarnings("unused")
	private String password;
	@SuppressWarnings("unused")
	private String name;
	public User(){
		
	}
	public User(String username, String password, String name) {
		
		this.username = username;
		this.password = password;
		this.name = name;
	}
	
	
}
